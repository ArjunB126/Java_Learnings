class TrafficAccidents {
    public static void main(String[] args) {
        byte fatal = 5;
        byte severe = 15;
        byte moderate = 30;
        byte minor = 50;
        byte noInjury = 70;

        System.out.println("Number of Fatal Accidents: " + fatal);
        System.out.println("Number of Severe Accidents: " + severe);
        System.out.println("Number of Moderate Accidents: " + moderate);
        System.out.println("Number of Minor Accidents: " + minor);
        System.out.println("Number of Accidents with No Injury: " + noInjury);
    }
}
