class Area { 

public static void main (String [] args)
{

	float radius = 5.5f;
	float pi = 3.14f;

	float circle_Perimeter = 2 * pi * radius; // 2pi r
	float circle_Area = pi * (radius * radius); // pi*radius*radius


	System.out.println("The Area Of Circle is :" + " " + circle_Area);
	System.out.println("The Area Of Circle is :" + " " + circle_Perimeter);
}



}