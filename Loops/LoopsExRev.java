class LoopsExRev{
	public static void main(String[] args) {


		for (char ch = 'Z'; ch >= 'A' ; ch -- ) {
			System.out.print(ch + " ");
		}

		

		for (char ch = 122; ch>=97 ; ch -- ) {
			System.out.print(ch + " ");
		}

		for (char ch = '9'; ch>= 48 ;ch -- ) {
			System.out.println(ch + " ");
			
		}


	}
}